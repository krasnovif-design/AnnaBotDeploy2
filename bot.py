async def handle_beck_result(update, context):
    text = update.message.text.strip()
    uid = update.effective_user.id

    match = re.search(r'бэкка\s*(\d+)', text, re.IGNORECASE)
    if not match:
        return False

    score = int(match.group(1))
    logger.info(f"Результат теста Бэкка от {uid}: {score}")

    if score <= 9:
        level = "отсутствие депрессивных симптомов"
        advice = "Твоё состояние в норме. Продолжай заботиться о себе: отдыхай, общайся, занимайся тем, что приносит радость."
        urgent = False
    elif score <= 15:
        level = "лёгкая депрессия (субдепрессия)"
        advice = "Обрати внимание на своё состояние. Попробуй вести дневник эмоций, больше гулять, общаться с близкими. Если симптомы не проходят – обратись к психологу."
        urgent = False
    elif score <= 19:
        level = "умеренная депрессия"
        advice = "Твоё состояние требует внимания. Рекомендую обратиться к психологу или психотерапевту. Также помогают регулярные прогулки, режим сна и дыхательные упражнения."
        urgent = True
    elif score <= 29:
        level = "выраженная депрессия (средней тяжести)"
        advice = "Это серьёзный сигнал. Настоятельно рекомендую обратиться к психиатру или психотерапевту. Ты не один, помощь доступна."
        urgent = True
    else:
        level = "тяжёлая депрессия"
        advice = "Твоё состояние требует немедленной профессиональной помощи. Пожалуйста, обратись к психиатру. Ты важен, и тебе помогут."
        urgent = True

    response = f"📊 **Результат теста Бэкка: {score} баллов**\n\n"
    response += f"Это соответствует **{level}**.\n\n"
    response += f"💡 {advice}\n\n"
    if urgent:
        response += "🆘 **Если тебе трудно справляться, не стесняйся обратиться за помощью.**\n\n"
    response += "💚 Я рядом. Расскажи, как ты себя чувствуешь прямо сейчас? Что происходит в твоей жизни?"

    await update.message.reply_text(response, parse_mode="Markdown", reply_markup=get_full_keyboard())

    # Сохраняем в память
    db.add_message(uid, "user", text, MAX_HISTORY)
    db.add_message(uid, "assistant", response, MAX_HISTORY)
    return True

