# ---------- ОСНОВНОЙ ОБРАБОТЧИК ----------
async def handle_message(update, context):
    logger.info(f"📩 Получено сообщение: {update.message.text}")

    # Проверяем результат теста Бэкка
    if await handle_beck_result(update, context):
        return

    # Проверяем команды техник
    if await handle_tech_command(update, context):
        return

    text = update.message.text
    uid = update.effective_user.id

    # Обработка кнопок
    if text == "💝 Сказать Анне спасибо":
        await update.message.reply_text(
            "🌷 **Спасибо, что вы со мной!**\n\n"
            "Если вы хотите отблагодарить Анну за поддержку, вы можете перевести средства на карту **ВТБ банка** по номеру:\n\n"
            "💳 **2200 2418 5707 2973**\n\n"
            "Каждая ваша поддержка помогает мне продолжать помогать другим. 💚\n\n"
            "С любовью, ваша Анна.",
            parse_mode="Markdown",
            reply_markup=get_base_keyboard()
        )
        return

    if text == "📋 Главное меню":
        await show_full_menu(update, context)
        return

    if text == "🔙 Назад":
        await go_back(update, context)
        return

    if text == "🧠 Тест на депрессию":
        await beck_test(update, context)
        return

    if text == "📜 Цитата дня":
        await quote_command(update, context)
        return

    if text == "🌱 Заземление":
        await grounding(update, context)
        return

    if text == "🧠 Техники КПТ":
        await show_cbt_techniques(update, context)
        return

    if text == "🔔 Включить напоминания":
        await remind_on(update, context)
        return

    if text == "🔕 Выключить напоминания":
        await remind_off(update, context)
        return

    if text == "🗑️ Очистить историю":
        await clear(update, context)
        return

    if text == "❓ Помощь":
        await help_command(update, context)
        return

    if "тревога" in text.lower() and "что делать" in text.lower():
        await handle_anxiety(update, context)
        return

    if text.startswith('/'):
        return

    # Обычный текст -> GPT
    db.add_message(uid, "user", text, MAX_HISTORY)
    history = db.get_history(uid)
    messages = [{"role": "system", "content": SYSTEM_PROMPT}] + history

    try:
        await context.bot.send_chat_action(chat_id=update.effective_chat.id, action="typing")
        response = await client.chat.completions.create(
            model="gpt-5.5",
            messages=messages,
            temperature=1.0,
            max_completion_tokens=500
        )
        reply = response.choices[0].message.content
        if not reply or reply.strip() == "":
            await update.message.reply_text(
                "Я тебя слышу, но не могу подобрать слова. Давай я пока предложу тебе упражнение /grounding?",
                reply_markup=get_base_keyboard()
            )
            return
        db.add_message(uid, "assistant", reply, MAX_HISTORY)
        await update.message.reply_text(reply, reply_markup=get_base_keyboard())

    except Exception as e:
        error_msg = str(e).lower()
        logger.error(f"GPT error: {e}")

        if "timeout" in error_msg or "connection" in error_msg or "network" in error_msg:
            text = "Мне нужно отойти на 5 минут, напиши чуть позже. 💚"
        elif "rate_limit" in error_msg or "quota" in error_msg or "exceeded" in error_msg:
            text = "Что-то пошло не так с моим подключением... Попробуй ещё раз через пару минут. 🌸"
        elif "api_key" in error_msg or "authentication" in error_msg or "permission" in error_msg:
            text = "У меня проблемы с доступом к серверу. Пожалуйста, сообщи об этом разработчику. 🙈"
        elif "model" in error_msg and "not found" in error_msg:
            text = "Я временно не могу использовать мою самую новую модель. Попробуй позже, я переключусь на резервную. 🧠"
        else:
            text = "Что-то пошло не так. Попробуй ещё раз или напиши /help. 🌷"

        await update.message.reply_text(text, reply_markup=get_base_keyboard())

# ---------- ПЛАНИРОВЩИК НАПОМИНАНИЙ ----------
async def send_weekly_reminder(app):
    now = datetime.now(pytz.timezone('Europe/Moscow'))
    current_time = now.strftime("%H:%M")
    current_day = now.weekday()
    for user in reminder.get_enabled():
        if user["day"] == current_day and user["time"] == current_time:
            try:
                await app.bot.send_message(
                    user["user_id"],
                    "🌷 Привет! Как прошла твоя неделя? Не забудь уделить время себе. Я рядом 💚"
                )
                reminder.update_last(user["user_id"])
            except Exception as e:
                logger.error(f"Не удалось отправить напоминание {user['user_id']}: {e}")

